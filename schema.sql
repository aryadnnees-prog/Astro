-- Políticas de RLS
-- Permitir lectura pública a categorías, productos y stock para la tienda online
create policy "Permitir lectura publica de categorias" on public.categorias for select using (true);
create policy "Permitir lectura publica de productos" on public.productos for select using (true);
create policy "Permitir lectura publica de stock" on public.stock for select using (true);

-- Permitir a usuarios autenticados realizar todas las acciones
create policy "Permitir todo a usuarios autenticados en categorias" on public.categorias for all using (auth.role() = 'authenticated');
create policy "Permitir todo a usuarios autenticados en productos" on public.productos for all using (auth.role() = 'authenticated');
create policy "Permitir todo a usuarios autenticados en stock" on public.stock for all using (auth.role() = 'authenticated');
create policy "Permitir todo a usuarios autenticados en ventas" on public.ventas for all using (auth.role() = 'authenticated');
create policy "Permitir todo a usuarios autenticados en pedidos" on public.pedidos_online for all using (auth.role() = 'authenticated');
create policy "Permitir todo a usuarios autenticados en perfiles" on public.perfiles for all using (auth.role() = 'authenticated');

-- Permitir a la tienda pública crear ventas y pedidos de manera anónima
create policy "Permitir crear ventas anonimamente" on public.ventas for insert with check (true);
create policy "Permitir crear pedidos anonimamente" on public.pedidos_online for insert with check (true);

-- Trigger para crear perfil automáticamente al registrarse en Supabase Auth
create or replace function public.handle_new_user()
returns trigger as $$
begin
  insert into public.perfiles (id, nombre, rol)
  values (new.id, coalesce(new.raw_user_meta_data->>'nombre', new.email), coalesce(new.raw_user_meta_data->>'rol', 'admin'));
  return new;
end;
$$ language plpgsql security definer;

-- Crear el trigger
drop trigger if exists on_auth_user_created on auth.users;
create trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();
